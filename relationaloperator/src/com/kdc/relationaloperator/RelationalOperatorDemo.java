package com.kdc.relationaloperator;

public class RelationalOperatorDemo {

    public static void main(String[] args) {
        // create variables
        int a = 7, b = 11;

        // == operator
        System.out.println(a == b);  // false

        // != operator
        System.out.println(a != b);  // true

        // > operator
        System.out.println(a > b);  // false

        // < operator
        System.out.println(a < b);  // true

        // >= operator
        System.out.println(a >= b);  // false

        // <= operator
        System.out.println(a <= b);  // true
    }
}
