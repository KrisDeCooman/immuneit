package com.kdc.assignmentoperator;

public class AssignmentDemo {

    public static void main(String[] args) {

        int x = 15;
        int y = x;

        System.out.println("The value of y is " + y);

        y+=x;
        System.out.println("The value of y is " + y);

        y%=x;
        System.out.println("The value of y is " + y);





    }
}
