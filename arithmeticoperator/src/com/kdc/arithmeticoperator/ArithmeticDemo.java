package com.kdc.arithmeticoperator;

public class ArithmeticDemo {


    public static void main(String[] args) {

        int a = 13;
        int b = 5;

        System.out.println("a + b is " + (a + b));
        System.out.println("a - b is " + (a - b));
        System.out.println("a * b is " + (a * b));
        System.out.println("a / b is " + (a / b));
        System.out.println("a % b is " + (a % b));
    }
}
